/**
 * FilingPulse - Team B Integration Mock Simulator
 * 
 * This simple script stands up a lightweight mock server on port 4000 (Team B's Webhook)
 * to test Team A's end-to-end data pipeline:
 * 1. Team A detects a change and POSTs the payload to Team B (Port 4000).
 * 2. This mock receives the payload, logs the details, and simulates an AI synthesis.
 * 3. This mock POSTs the synthesized alert back to Team A's receiver (Port 3000).
 * 4. Team A processes, saves to DB, and streams it to the React Frontend in real-time.
 */

const http = require('http');

const PORT = 4000;
const TEAM_A_ALERT_URL = 'http://localhost:3000/api/alerts';

const server = http.createServer((req, res) => {
  if (req.method === 'POST' && req.url === '/api/changes-receiver') {
    let body = '';
    
    req.on('data', chunk => {
      body += chunk.toString();
    });
    
    req.on('end', () => {
      try {
        const payload = JSON.parse(body);
        console.log('\n==================================================');
        console.log('📥 RECEIVED CHANGE PAYLOAD FROM TEAM A WEBHOOK:');
        console.log(`Company: ${payload.companyName} (${payload.ticker})`);
        console.log(`Source URL: ${payload.sourceUrl}`);
        console.log(`Normalized Diff Preview:\n${payload.diff}`);
        console.log('==================================================');

        // Simulate Team B AI Agent synthesis delay (1.5 seconds)
        console.log('🤖 Simulating AI Analysis and synthesis...');
        setTimeout(() => {
          const simulatedAlert = {
            companyId: payload.companyId,
            title: `AI Synthesized Alert: ${payload.ticker} Key Shift`,
            summary: `This is an alert generated by Team B's AI simulation for ${payload.companyName}.`,
            whatChanged: `A difference was detected between snapshots.\nDiff details:\n${payload.diff}`,
            whyItMatters: `Changes on these regulatory/IR pages are critical signals of operational restructuring or major financial announcements.`,
            confidence: 0.94,
            severity: payload.diff.toLowerCase().includes('appoint') || payload.diff.toLowerCase().includes('departure') ? 'HIGH' : 'MEDIUM',
            sourceLink: payload.sourceUrl
          };

          sendAlertToTeamA(simulatedAlert);
        }, 1500);

        res.writeHead(200, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ success: true, message: 'Changes received and queued for synthesis.' }));
      } catch (err) {
        console.error('❌ Failed to parse payload:', err.message);
        res.writeHead(400, { 'Content-Type': 'application/json' });
        res.end(JSON.stringify({ error: 'Invalid JSON payload' }));
      }
    });
  } else {
    res.writeHead(404, { 'Content-Type': 'application/json' });
    res.end(JSON.stringify({ error: 'Endpoint not found' }));
  }
});

function sendAlertToTeamA(alertPayload) {
  const url = new URL(TEAM_A_ALERT_URL);
  
  const options = {
    hostname: url.hostname,
    port: url.port,
    path: url.pathname,
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    }
  };

  console.log(`📤 Sending Synthesized Alert back to Team A (${TEAM_A_ALERT_URL})...`);

  const req = http.request(options, (res) => {
    let responseData = '';
    res.on('data', (chunk) => responseData += chunk);
    res.on('end', () => {
      console.log(`✅ Team A response: ${res.statusCode} - ${responseData.trim()}`);
      console.log('==================================================\n');
    });
  });

  req.on('error', (e) => {
    console.error(`❌ Failed to send alert back to Team A: ${e.message}`);
  });

  req.write(JSON.stringify(alertPayload));
  req.end();
}

server.listen(PORT, () => {
  console.log(`🚀 Mock Team B Webhook Server running on http://localhost:${PORT}`);
  console.log(`Listening for changes at POST http://localhost:${PORT}/api/changes-receiver`);
});
